    private void buscarButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buscarButtonMousePressed
        String dniString = jTextField1.getText().trim();
        String errorString = "";
        boolean error = false;
        Pattern patron = Pattern.compile("^[1-9]\\d{6,9}$");
        Matcher matcher1 = patron.matcher(dniString);
        if (matcher1.matches()){
            JTable auxTable = TurnoManager.getInstance().getFuturePatientAppointments(Long.parseLong(dniString));
            turnosTable.setModel(auxTable.getModel());
            this.removeColumn();
            if(turnosTable.getRowCount()==0){
                error = true;
                errorString = errorString + "No existe un turno futuro para ese paciente \n";
            }
        } else {
            error = true;
            jTextField1.setText("");
            errorString = errorString + "DNI Ingresado invalido \n";
        }
        if (error){
            JOptionPane.showMessageDialog(null, "Error!\n" + errorString);
        }



    }//GEN-LAST:event_buscarButtonMousePressed

    private void confirmarButtonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_confirmarButtonMousePressed
        Object[] options = {"SÃ­", "No"};

        String[] optionsStr = new String[options.length];
        for (int i = 0; i < options.length; i++) {
            optionsStr[i] = options[i].toString();
        }

        int resp = JOptionPane.showOptionDialog(this,
                "Usted esta a punto de eliminar el turno.\n"+"Â¿Esta seguro?",
                "Alerta!",
                JOptionPane.DEFAULT_OPTION,
                JOptionPane.WARNING_MESSAGE,
                null,
                optionsStr,
                optionsStr[1]);

        if(resp == 0){
            // code for yes response
            int row = turnosTable.getSelectedRow();
            turnosTable.addColumn(idsTurnos);
            Turno turno = TurnoManager.getInstance().getById(Integer.parseInt(turnosTable.getValueAt(row,5).toString()));
            turnosTable.removeColumn(idsTurnos);
            TurnoManager.getInstance().deleteTurno(turno);
            confirmarButton.setBackground(new Color (223, 246, 255) );
            PanelGUIHandler.getinstance().changePanel(PanelGUIHandler.panelTurnos,null);
            SMenuGUIHandler.getInstance().changePanel(SMenuGUIHandler.menuSecundarioVacio,null);
        }else{
            //code respuesta no
        }

    }//GEN-LAST:event_confirmarButtonMousePressed

